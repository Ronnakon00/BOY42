#include <stdio.h>

int ft_sqrt(int nb)
{
    int i;
    if (nb <= 0)
        return (0);
    
    i = 1;
    while (i <= nb / 2)
    {
        if (i * i == nb)
            return (i);
        i++;
    }
    return (0);
}

int main()
{
    int x;
    x = ft_sqrt(144);
    printf("%d",x);
}